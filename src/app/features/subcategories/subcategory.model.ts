/**
 * Модель данных для подкатегории
 * Представляет подкатегорию внутри категории (item)
 */
export interface Subcategory {
  /** Уникальный идентификатор подкатегории (UUID) */
  id: string;
  
  /** ID родительской категории */
  item_id: string;
  
  /** Название подкатегории */
  title: string;
  
  /** Порядок сортировки внутри категории (для drag-and-drop) */
  sortOrder?: number;
  
  /** Дата и время создания в формате ISO 8601 */
  createdAt: string;
  
  /** Дата и время последнего обновления в формате ISO 8601 */
  updatedAt: string;
}



