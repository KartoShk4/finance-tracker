/**
 * Модель данных для транзакции
 * Представляет отдельную операцию дохода или расхода
 */
export interface Transaction {
  /** Уникальный идентификатор транзакции (UUID) */
  id: string;
  
  /** ID категории, к которой относится транзакция (foreign key) */
  item_id: string;
  
  /** Тип транзакции: доход или расход */
  type: 'income' | 'expense';
  
  /** Сумма транзакции */
  amount: number;
  
  /** Дата и время транзакции в формате ISO 8601 */
  date: string;
  
  /** Примечание к транзакции (необязательное) */
  notes?: string;
}
