/**
 * Модель данных для категории (Item)
 * Представляет категорию доходов или расходов
 */
export interface Item {
  /** Уникальный идентификатор категории (UUID) */
  id: string;
  
  /** Название категории */
  title: string;
  
  /** Тип категории: доход или расход (опциональный, так как тип определяется транзакциями) */
  category?: 'income' | 'expense';
  
  /** Общая сумма всех транзакций в этой категории */
  total: number;
  
  /** Дата и время последнего обновления в формате ISO 8601 */
  lastUpdated: string;
  
  /** Является ли категория избранной */
  isFavorite?: boolean;
  
  /** Порядок сортировки (для drag-and-drop) */
  sortOrder?: number;
}
